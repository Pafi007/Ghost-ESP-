// BLE Helper Functions
// ============================================================
void generateRandomMAC(esp_bd_addr_t addr) {
  for (int i = 0; i < 6; i++) addr[i] = random(256);
  addr[0] |= 0xF0;
}

void randomizeTxPower() {
  int r = random(100);
  if (r < 70) esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, MAX_TX_POWER);
  else if (r < 85) esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, (esp_power_level_t)(MAX_TX_POWER-1));
  else if (r < 95) esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, (esp_power_level_t)(MAX_TX_POWER-2));
  else if (r < 99) esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, (esp_power_level_t)(MAX_TX_POWER-3));
  else esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, (esp_power_level_t)(MAX_TX_POWER-4));
}

void ensureBLEInit() {
  if (!bleInitialized) {
    BLEDevice::init("");
    esp_bt_controller_mem_release(ESP_BT_MODE_CLASSIC_BT);
    advertising = BLEDevice::getAdvertising();
    esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, MAX_TX_POWER);
    bleInitialized = true;
  }
}

void sendBLEPacket(BLEAdvertisementData &advData) {
  ensureBLEInit();
  radioTxBusy = true;
  esp_bd_addr_t addr;
  generateRandomMAC(addr);
  int advType = random(3);
  if (advType==0) advertising->setAdvertisementType(ADV_TYPE_IND);
  else if (advType==1) advertising->setAdvertisementType(ADV_TYPE_SCAN_IND);
  else advertising->setAdvertisementType(ADV_TYPE_NONCONN_IND);
  advertising->setDeviceAddress(addr, BLE_ADDR_TYPE_RANDOM);
  advertising->setAdvertisementData(advData);
  advertising->setMinInterval(0x20); advertising->setMaxInterval(0x20);
  advertising->setMinPreferred(0x20); advertising->setMaxPreferred(0x20);
  digitalWrite(BLUE_LED_PIN, HIGH);
  advertising->start();
  delay(150);
  advertising->stop();
  digitalWrite(BLUE_LED_PIN, LOW);
  radioTxBusy = false;
  randomizeTxPower();
  packetCount++;
}

